    private void TblPelangganMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TblPelangganMouseClicked
        int row = TblPelanggan.getSelectedRow();
        id_pelanggan.setText(TblPelanggan.getModel().getValueAt(row, 0).toString());
        nama.setText(TblPelanggan.getModel().getValueAt(row, 1).toString());
        String gender = tabmode.getValueAt(row, 2).toString();
        if("L".equals(gender)){
            laki.setSelected(true);
        }else{
            perempuan.setSelected(true);
        }
        alamat.setText(TblPelanggan.getModel().getValueAt(row, 3).toString());
        noHp.setText(TblPelanggan.getModel().getValueAt(row, 4).toString());
    }//GEN-LAST:event_TblPelangganMouseClicked

    private void Delete1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Delete1ActionPerformed
        if(JOptionPane.showConfirmDialog(null,"Hapus Data Pelanggan","U-Laundry", JOptionPane.YES_NO_OPTION)==JOptionPane.YES_OPTION){
        String pelanggan = nama.getText();
        try{
            if(pelanggan.isEmpty()){
                JOptionPane.showMessageDialog(null,"Data belum dipilih ");
            }else{
                 String sql = "delete from pelanggan where id_pelanggan = '"+id_pelanggan.getText()+"'";
            try {
                PreparedStatement stat = conn.prepareStatement(sql);
                stat.executeUpdate();
                JOptionPane.showMessageDialog(null, "data berhasil dihapus");
                clear();
            }
            catch (SQLException e){
                JOptionPane.showMessageDialog(null, "data gagal dihapus" +e);
            }
            datatable();
            }
        }catch(Exception e){
            
        }
        }
    }//GEN-LAST:event_Delete1ActionPerformed

