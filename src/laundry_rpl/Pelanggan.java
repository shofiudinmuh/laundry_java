    private void SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveActionPerformed
            String gender = null;
               if (laki.isSelected()){
                        gender = "L";
                   } else if (perempuan.isSelected()){
                        gender = "P";
                   }
               String nm= nama.getText();
        try{
            if(gender ==null){
                JOptionPane.showMessageDialog(null, "Pilih Jenis Kelamin");
            }
                if(nm.isEmpty()){
                    JOptionPane.showMessageDialog(null, "Masukkan nama");
                }else{
                    
                
               String sql = "insert pelanggan values (?,?,?,?,?)";
        
               try {
                        PreparedStatement stat = conn.prepareStatement(sql);
//                        autonumber();
                        stat.setString(1,id_pelanggan.getText());
                        stat.setString(2,nama.getText());
                        stat.setString(3,gender);
                        stat.setString(4,alamat.getText());
                        stat.setString(5,noHp.getText());
            
            
                        stat.executeUpdate();
                        JOptionPane.showMessageDialog(null, "Sukses");
                        clear();
                   }
               catch (SQLException e){
                        JOptionPane.showMessageDialog(null, "data gagal disimpan" +e);
                   }
               datatable();
               autonumber();
                }
        }catch(Exception a){
            
        }
    }//GEN-LAST:event_SaveActionPerformed

    private void EditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditActionPerformed
        if(JOptionPane.showConfirmDialog(null,"Ubah Data Pelanggan","U-Laundry", JOptionPane.YES_NO_OPTION)==JOptionPane.YES_OPTION){
            String pelanggan = nama.getText();
            try{
            if(pelanggan.isEmpty()){
                JOptionPane.showMessageDialog(null,"Data belum dipilih ");
            }else{
        String gender = null;
               if (laki.isSelected()){
                        gender = "L";
                   } else if (perempuan.isSelected()){
                        gender = "P";
                   }
        
               String sql = "update pelanggan set id_pelanggan=?,nama_pelanggan=?,gender=?,"
                       + "alamat=?,no_hp=? where id_pelanggan='"+id_pelanggan.getText()+"'";
        
               try {
                        PreparedStatement stat = conn.prepareStatement(sql);
//                        autonumber();
                        stat.setString(1,id_pelanggan.getText());
                        stat.setString(2,nama.getText());
                        stat.setString(3,gender);
                        stat.setString(4,alamat.getText());
                        stat.setString(5,noHp.getText());
            
            
                        stat.executeUpdate();
                        JOptionPane.showMessageDialog(null, "data berhasil disimpan");
                        clear();
                   }
               catch (SQLException e){
                        JOptionPane.showMessageDialog(null, "data gagal disimpan" +e);
                   }
               datatable();  
        }
            }catch(Exception e){
            
        }
        }
    }//GEN-LAST:event_EditActionPerformed

